package io.boodskap.iot.model.jpa;

import javax.persistence.EmbeddedId;
import javax.persistence.Entity;
import javax.persistence.Table;

import io.boodskap.iot.model.ICameraDevice;

@Entity
@Table(name="cameradevice")
public class CameraDevice extends AbstractModel implements ICameraDevice {
	
	private static final long serialVersionUID = -9188154426472109251L;

	@EmbeddedId
	private CameraDeviceId id = new CameraDeviceId();

	public CameraDevice() {
	}

	public CameraDevice(CameraDeviceId id) {
		this.id = id;
	}

	public String getDomainKey() {
		return id.getDomainKey();
	}

	public void setDomainKey(String domainKey) {
		id.setDomainKey(domainKey);
	}

	public String getDeviceId() {
		return id.getDeviceId();
	}

	public void setDeviceId(String deviceId) {
		id.setDeviceId(deviceId);
	}

	public String getCamera() {
		return id.getCamera();
	}

	public void setCamera(String camera) {
		id.setCamera(camera);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		CameraDevice other = (CameraDevice) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}
